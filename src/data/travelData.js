// Get travel safety for a country — checks override first, then falls back to geopolitical risk mapping
export function getTravelSafety(countryName) {
  if (TRAVEL_SAFETY_OVERRIDE[countryName]) return TRAVEL_SAFETY_OVERRIDE[countryName];
  const data = COUNTRIES[countryName];
  if (!data?.risk) return SAFETY_RATING_MAP.clear;
  return SAFETY_RATING_MAP[data.risk] || SAFETY_RATING_MAP.clear;
}

